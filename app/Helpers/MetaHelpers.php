    /*
     * Set metatag and build html
     */
    public function setMetaTag($title = null, $description = null, $keywords = null, $og_url = null, $og_sitename = null, $og_ogp = null)
    {
        $route = request()->route()->getName();
        if (isset($this->metaScreen[$route])) {
            if ($title) {
                $this->metaScreen[$route]['title'] = $title;
            }
            if ($description) {
                $this->metaScreen[$route]['description'] = $description;
            }
            if ($keywords) {
                $this->metaScreen[$route]['keywords'] = $keywords;
            }
            if ($og_url) {
                $this->metaScreen[$route]['og:url'] = $og_url;
            }
            if ($og_sitename) {
                $this->metaScreen[$route]['og:site_name'] = $og_sitename;
            }
            if ($og_ogp) {
                $this->metaScreen[$route]['og:ogp'] = $og_ogp;
            }
            return $this->buildMetaHtml($this->metaScreen[$route]);
        }
    }


    public function buildMetaHtml($metas)
    {
        $metaHtlm = '';
        $metaHtlm .= '<title>' . $metas['title'] . '</title>';
        $metaHtlm .= '<meta name="description" content="' . $metas['description'] . '">';
        $metaHtlm .= '<meta name="keywords" content="' . $metas['keywords'] . '">';
        $metaHtlm .= '<meta property="og:type" content="article">';
        $metaHtlm .= '<meta property="og:local" content="ja_JP">';
        $metaHtlm .= '<meta property="og:title" content="' . $metas['title'] . '">';
        $metaHtlm .= '<meta property="og:description" content="' . $metas['description'] . '">';
        $metaHtlm .= '<meta property="og:url" content="' . $metas['og:url'] . '">';
        $metaHtlm .= '<meta property="og:image" content="' . $metas['og:ogp'] . '">';
        $metaHtlm .= '<meta property="og:site_name" content="' . $metas['og:site_name'] . '">';

        $metaHtlm .= '<meta name="twitter:card" content="summary_large_image">';
        $metaHtlm .= '<meta property="twitter:title" content="' . $metas['title'] . '">';
        $metaHtlm .= '<meta property="twitter:description" content="' . $metas['description'] . '">';
        $metaHtlm .= '<meta property="twitter:image" content="' . $metas['og:ogp'] . '">';
        $metaHtlm .= '<link rel="canonical" href="' . $metas['og:url'] . '">';

        return $metaHtlm;
    }


